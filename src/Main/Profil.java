    private void resetPasswordButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetPasswordButtonActionPerformed
        try {
            // Mendapatkan NIM dari session manager
            String nim = SessionManager.getInstance().getNim();
            
            // Mendapatkan ID pengguna dari mahasiswaDAO
            MahasiswaDAO mahasiswaDAO = new MahasiswaDAO();
            int idUser = mahasiswaDAO.getIdUserByNim(nim);
            
            // Mendapatkan password lama dari JPasswordField
            char[] lamaPasswordChars = lamaPasswordField.getPassword();
            String lamaPassword = new String(lamaPasswordChars);
            
            
            if (mahasiswaDAO.authenticate(nim, lamaPassword)) {
                // Password lama cocok, lanjutkan dengan verifikasi password baru
                char[] baruPasswordChars = baruPasswordField.getPassword();
                char[] baru2PasswordChars = baru2PasswordField.getPassword();
                String baruPassword = new String(baruPasswordChars);
                String baru2Password = new String(baru2PasswordChars);
                
                // Verifikasi kedua password baru harus sesuai
                if (baruPassword.equals(baru2Password)) {
                    // Lakukan hashing password baru sebelum menyimpan ke database
                    String hashedPassword = hashPassword(baruPassword);
                    
                    // Melakukan update password menggunakan UserModel
                    boolean updateSuccess = mahasiswaDAO.updatePassword(idUser, hashedPassword);
                    
                    if (updateSuccess) {
                        JOptionPane.showMessageDialog(null, "Password berhasil direset!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                        // Reset kolom password setelah berhasil
                        lamaPasswordField.setText("");
                        baruPasswordField.setText("");
                        baru2PasswordField.setText("");
                        
                        //profil
                        mainPanel.removeAll();
                        mainPanel.add(profil);
                        mainPanel.repaint();
                        mainPanel.revalidate();
                    } else {
                        JOptionPane.showMessageDialog(null, "Gagal mereset password.", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Password baru tidak cocok.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Password lama salah.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Profil.class.getName()).log(Level.SEVERE, null, ex);
        }

    
    }//GEN-LAST:event_resetPasswordButtonActionPerformed

