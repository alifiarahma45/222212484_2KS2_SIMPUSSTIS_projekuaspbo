    private void loadData() {
        String nim = SessionManager.getInstance().getNim();
        if (nim != null) {
            try {
                MahasiswaDAO mahasiswaDAO = new MahasiswaDAO();
                Mahasiswa mahasiswa = mahasiswaDAO.getMahasiswaByNim(nim);

                if (mahasiswa != null) {
                    nimTextField.setText(mahasiswa.getNim());
                    namaTextField.setText(mahasiswa.getNama());
                    jkTextField.setText(mahasiswa.getJk());
                    kelasTextField.setText(mahasiswa.getKelas());
                } else {
                    JOptionPane.showMessageDialog(this, "Data mahasiswa tidak ditemukan!");
                }
            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Gagal memuat data mahasiswa: " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "NIM tidak ditemukan dalam sesi!");
        }
    }


